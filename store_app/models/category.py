from django.db import models
from store_app.models import BaseModel


class Category(BaseModel):
    """
    Модель категории товаров.

    Наследует общие поля от BaseModel (название, описание, даты создания и обновления).
    Представляет собой категорию, к которой могут быть отнесены товары.

    Используется для:
    - группировки товаров по тематическим категориям;
    - построения навигационного меню;
    - фильтрации и поиска товаров по категориям.
    """

    name = models.CharField(
        max_length=100,
        verbose_name="Товарная категория",
    )
    """
    Название категории товаров.

    Отображается в интерфейсе магазина и используется для идентификации категории.

    Attributes:
        max_length (int): Максимальная длина строки — 100 символов.
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора
            и форм. Отображается в админ‑панели Django и в автоматически
            генерируемых формах.
    """
