from django.db import models
from store_app.models import BaseModel
from .category import Category


class Product(BaseModel):
    """
    Модель товара.
    Наследует общие поля от BaseModel (описание, даты создания и обновления).
    Описывает отдельный товар с его характеристиками: наименованием, ценой и категорией.
    """

    name = models.CharField(
        max_length=100,
        verbose_name="Наименование товара",
    )
    """
    Наименование товара.

    Отображается в каталоге товаров, карточках товара и результатах поиска.

    Attributes:
        max_length (int): Максимальная длина строки — 100 символов.
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора
            и форм. Используется в админ‑панели Django и автоматически
            генерируемых формах.
    """

    price = models.FloatField(
        verbose_name="Цена за единицу"
    )
    """
    Цена товара за одну единицу измерения.

    Attributes:
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
            Отображается в админ‑панели и формах как «Цена за единицу».
    """

    category = models.ForeignKey(
        Category,
        related_name="products",
        blank=True,
        null=True,
        on_delete=models.DO_NOTHING,
        verbose_name="Товарная категория",
    )
    """
    Связь с категорией товара.

    Определяет принадлежность товара к определённой категории в каталоге.
    Поле необязательно — товар может существовать без привязки к категории.

    Attributes:
        Category (Model): Целевая модель, на которую ссылается внешний ключ.
        related_name (str): Имя обратной связи от модели Category к Product.
            Позволяет получать все товары категории через category.products.all().
        blank (bool): Разрешает оставлять поле пустым в формах (на уровне интерфейса).
        null (bool): Разрешает хранить NULL в базе данных (на уровне БД).
        on_delete (str): Поведение при удалении связанной категории.
            DO_NOTHING — не предпринимать действий при удалении категории.
            Требует дополнительной обработки на уровне приложения.
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
    """
