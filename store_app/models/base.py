from django.db import models


class BaseModel(models.Model):
    """
    Абстрактная базовая модель для наследования другими моделями приложения.

    Содержит общие поля, которые требуются в большинстве моделей:
    - название;
    - описание;
    - временные метки создания и обновления.

    Не создаёт отдельную таблицу в базе данных благодаря атрибуту abstract = True.
    Предназначена для повторного использования в моделях приложения через наследование.
    """

    class Meta:
        """
        Метаданные модели, определяющие её абстрактный статус.
        """

        abstract = True
        # Флаг, указывающий, что модель является абстрактной.

    name = models.CharField(
        max_length=100, verbose_name="Название"
    )
    """
    Название объекта.

    Attributes:
        max_length (int): Максимальная длина строки — 100 символов.
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
    """

    description = models.TextField(verbose_name="Описание")
    """
    Текстовое описание объекта.

    Attributes:
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
    """

    created_at = models.DateTimeField(
        auto_now_add=True, verbose_name="Создано"
    )
    """
    Дата и время создания записи.

    Устанавливается один раз при создании объекта и не изменяется.

    Attributes:
        auto_now_add (bool): Автоматически устанавливает текущее время
            при создании объекта.
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
    """

    updated_at = models.DateTimeField(
        auto_now=True, verbose_name="Изменено"
    )
    """
    Дата и время последнего обновления записи.

    Автоматически обновляется при каждом сохранении объекта.

    Attributes:
        auto_now (bool): Автоматически обновляет поле до текущего времени
            при каждом вызове save().
        verbose_name (str): Человекочитаемое имя поля для интерфейса администратора.
    """

    def __str__(self):
        """
        Возвращает строковое представление объекта.

        Используется в интерфейсе администратора Django и при выводе объектов
        в шаблонах. В качестве строки используется значение поля name.

        Returns:
            str: Название объекта (значение поля name).
        """
        return self.name

    def __repr__(self):
        """
        Возвращает официальное строковое представление объекта.

        Используется для отладки и логирования. Делегирует вызов методу __str__,
        чтобы обеспечить единообразное представление объекта в разных контекстах.

        Returns:
            str: Строковое представление объекта, возвращаемое методом __str__.
        """
        return self.__str__()
